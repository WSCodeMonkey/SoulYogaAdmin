package com.web.soulyogaadmin.course.vo;

public class CourseConditionVO {

	private String name;
	
	private Integer courseCategoryID;
	
	private Integer yogaClubID;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getCourseCategoryID() {
		return courseCategoryID;
	}

	public void setCourseCategoryID(Integer courseCategoryID) {
		this.courseCategoryID = courseCategoryID;
	}

	public Integer getYogaClubID() {
		return yogaClubID;
	}

	public void setYogaClubID(Integer yogaClubID) {
		this.yogaClubID = yogaClubID;
	}

	
	
}
